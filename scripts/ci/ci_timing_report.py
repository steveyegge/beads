def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--repo", default="gastownhall/beads")
    parser.add_argument("--runs", type=int, default=120)
    parser.add_argument("--out-dir", type=Path, default=Path("docs/reports"))
    parser.add_argument("--workers", type=int, default=8)
    args = parser.parse_args()

    root = Path.cwd()
    workflows = workflow_file_summary(root)
    runs = collect_runs(args.repo, args.runs)
    selected = [run for run in runs if run.get("status") == "completed"]

    jobs_by_run: dict[int, list[dict[str, Any]]] = {}
    with concurrent.futures.ThreadPoolExecutor(max_workers=args.workers) as executor:
        future_to_run = {executor.submit(collect_jobs, args.repo, run["databaseId"]): run for run in selected}
        for future in concurrent.futures.as_completed(future_to_run):
            run = future_to_run[future]
            jobs_by_run[run["databaseId"]] = future.result()

    summary = summarize(runs, jobs_by_run)
    markdown = render_markdown(args.repo, args.runs, summary, workflows)
    html_report = render_html(markdown)

    args.out_dir.mkdir(parents=True, exist_ok=True)
    md_path = args.out_dir / "ci-timing-report.md"
    html_path = args.out_dir / "ci-timing-report.html"
    md_path.write_text(markdown, encoding="utf-8")
    html_path.write_text(html_report, encoding="utf-8")
    print(f"wrote {md_path}")
    print(f"wrote {html_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
